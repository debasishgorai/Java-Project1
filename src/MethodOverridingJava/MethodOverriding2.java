package MethodOverridingJava;
class father{
	public void tvs() {
		System.out.println("fiityggggxtgdvd");
	}
}
class son extends father{
	public void tvs() {
		System.out.println("dddffgfg");
	}
}
public class MethodOverriding2 {
public static void main(String[]args) {
	son son = new son();
	son.tvs();
	
}
}
